package com.warzone.edu_assist_domain_task;

public class Task_Completion {
	private int task_id;
	private String student_id;
	private String student_name;
	private String complete_status;
	private String task_position;
	private String classa;
	public int getTask_id() {
		return task_id;
	}
	public void setTask_id(int task_id) {
		this.task_id = task_id;
	}
	public String getStudent_id() {
		return student_id;
	}
	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public String getComplete_status() {
		return complete_status;
	}
	public void setComplete_status(String complete_status) {
		this.complete_status = complete_status;
	}
	public String getTask_position() {
		return task_position;
	}
	public void setTask_position(String task_position) {
		this.task_position = task_position;
	}
	public String getClassa() {
		return classa;
	}
	public void setClassa(String classa) {
		this.classa = classa;
	}
	
	
}
